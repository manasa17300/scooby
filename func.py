print("using add function")
def add(x,y):
    return x+y
print((add(2,7)))

print("using sub function")
def sub(x,y):
    return x-y
print((sub(7,2)))

print("using mul function")
def mul(x,y):
    return x*y
print((mul(7,2)))


print("using div function")
def div(x,y):
    return x/y
print((div(25,5)))
